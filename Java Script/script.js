//test for (type of function)

//When a is number
var a = 10;
console.log("this is a", a);
console.log(typeof a);

//when a is a string
a="aparna";
console.log("value of a is",a);
console.log(typeof a);

//when a is a boolean
a=true;
console.log("value of a is",a);
console.log(typeof a);

//when a is undefined
let b;
console.log("value of b is",b);
console.log(typeof b);

//when a is null
let n=null;
console.log("value of n is",n);
console.log(typeof n);

//when a is symbol
let sym=Symbol("id");
console.log("this is symbol",sym);
console.log(typeof sym);

//when a is BigInt
let BigInt=3267382653675834649836n;
console.log("this is bigint", BigInt);
